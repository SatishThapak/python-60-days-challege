# Arithmetic Operators

a, b = 15, 4
print(a + b)  # 19
print(a // b) # 3

# Assignment Operators
x = 10
x += 5  # x = 15

# Logical Operators

a, b = True, False
print(a and b) # False
print(a or b)  # True
print(not a)   # False

# Identity Operators
x = [1,2,3]
y = x
z = [1,2,3]
print(x is y)  # True
print(x is z)  # False (different objects)
